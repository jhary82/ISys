# Print-Function
# borderLow: untere Grenze
# borderHigh: obere Grenze
# max: Anzahl aller Elemente
# column: zu verarbeitende Spalte
# title: Titel für Knoten
print <- function( borderLow, borderHigh,  max, column, title){
	f <- subset( column, (column >= borderLow & column < borderHigh)) 
	value <- length( f ) / max	
	cat("<",title, " low='", borderLow, "' high='", borderHigh, "' value='", value, "'/>\n", sep="")
}

